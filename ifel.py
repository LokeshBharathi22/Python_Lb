mark=int(input("Mark:"))


if(mark==100):
    print("O+ grade",mark)

elif(mark>=90):
    print("O grade",mark)

elif(mark>=80):
    print("A grade",mark)

elif(mark>=70):
    print("B grade",mark)

elif(mark>=60):
    print("C grade",mark)

elif(mark>=50):
    print("D grade",mark)

else:
    print("fail")